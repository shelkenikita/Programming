class substract
{
    public static int sub(int a, int b, int c, int d)
    {
        int e=a-b-c-d;
        return e;
    }

    public static void main(String[]args)
    {
        System.out.println("MB");
        int r=sub(90,10,20,30);
        System.out.println(r);
        System.out.println("ME");
    }
}